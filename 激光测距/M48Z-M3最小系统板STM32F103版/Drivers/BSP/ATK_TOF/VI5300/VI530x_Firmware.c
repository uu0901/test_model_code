/**
 * @brief   VI530X 获取固件运行状态
 * @param   无 
 * @return  0-固件成功运行 && I2C读写无异常;other-异常（固件没有成功运行 || I2C读写有异常）
 */
VI530x_Status Get_VI530x_Download_Firmware_Status(void)
{
  uint8_t value = 0;
  VI530x_Status ret = VI530x_OK;

    ret |= atk_tof_read_byte(0x08, &value);

    if(0x66 == value || 0xAA == value)
    {
        ret &= ~VI530x_ERROR_FW_FAILURE;
    }
    else
    {
        ret |= VI530x_ERROR_FW_FAILURE;
    }
    return ret;
}

/**
 * @brief   VI530X 下固件前的配置
 * @param   无
 * @return  0-操作成功（I2C读写无异常）;other-异常（I2C读写有异常）
 */
VI530x_Status VI530x_Write_Firmware_PreConfig(void)
{
    VI530x_Status ret = VI530x_OK;
    
    uint8_t reg_sys_cfg = 0;
    
    ret |= VI530x_Set_Digital_Clock_Dutycycle();

    ret |= atk_tof_write_byte(VAN_REG_PW_CTRL, 0x08);

    ret |= atk_tof_write_byte(VAN_REG_PW_CTRL, 0x0a);

    ret |= atk_tof_write_byte(VAN_REG_MCU_CFG, 0x06); // 0x02

    ret |= atk_tof_read_byte(VAN_REG_SYS_CFG, &reg_sys_cfg);

    ret |= atk_tof_write_byte(VAN_REG_SYS_CFG, reg_sys_cfg | (0x01 << 0));

    ret |= atk_tof_write_byte(VAN_REG_CMD, 0x01);

    ret |= atk_tof_write_byte(VAN_REG_SIZE, 0x02);

    ret |= atk_tof_write_byte(VAN_REG_SCRATCH_PAD_BASE + 0x00, 0x00);

    ret |= atk_tof_write_byte(VAN_REG_SCRATCH_PAD_BASE + 0x01, 0x00);

    return ret;
}

/**
 * @brief 	VI530X 写完固件后的配置
 * @param 	[none] 
 * @return 	[uint8_t]	ret:0-操作成功（I2C读写无异常）;other-异常（I2C读写有异常）
 */
VI530x_Status VI530x_Write_Firmware_Post_Config(void)
{
    VI530x_Status ret = VI530x_OK;
    //#define VI530x_POWER_MANAGE       1
    if (VI530x_Cali_Data.VI530x_Power_Manage_Status)
    {
            //开启电源管理模式
            ret |= atk_tof_write_byte(VAN_REG_SYS_CFG, 0x0C);
    }
    else
    {
            //关闭启电源管理模式
            ret |= atk_tof_write_byte(VAN_REG_SYS_CFG, 0x08);
    }

    ret |= atk_tof_write_byte(VAN_REG_MCU_CFG, 0x06);

    ret |= atk_tof_write_byte(VAN_REG_AO_DOMAIN, 0xA0);

    ret |= atk_tof_write_byte(VAN_REG_AO_DOMAIN, 0x80);

    ret |= atk_tof_write_byte(VAN_REG_MCU_CFG, 0x07); // 0x03

    ret |= atk_tof_write_byte(VAN_REG_PW_CTRL, 0x02);

    ret |= atk_tof_write_byte(VAN_REG_PW_CTRL, 0x00);

    return ret;
}

/**
 * @brief   VI530X 写固件
 * @param   *Firmware_buff：固件数据地址
 * @param   size：固件长度
 * @return  0-操作成功（I2C读写无异常）;other-异常（I2C读写有异常）
 */

VI530x_Status VI530x_Download_Firmware(uint8_t *Firmware_buff, uint16_t size)
{
    VI530x_Status ret = VI530x_OK;
    uint8_t page = 0;

    uint8_t temp_buff[2];

    ret |= VI530x_Write_Firmware_PreConfig();

    while (size >= 32)
    {
        temp_buff[0] = VAN_WRITEFW_CMD;
        temp_buff[1] = 32;

        ret |= atk_tof_write_nbytes(VAN_REG_CMD, temp_buff, 2);

        ret |= atk_tof_write_nbytes(VAN_REG_SCRATCH_PAD_BASE, Firmware_buff + page * 32, 32);
//        //该延时理论上是需要的，实测不加延时也可以
//        Rough_Delay(5);
//        delay_ms(1);
        size -= 32;
        page++;
    }

    if (size > 0)
    {
            temp_buff[0] = VAN_WRITEFW_CMD;
            temp_buff[1] = 32;

            ret |= atk_tof_write_nbytes(VAN_REG_CMD, temp_buff, 2);

            ret |= atk_tof_write_nbytes(VAN_REG_SCRATCH_PAD_BASE, Firmware_buff + page * 32, 32);
    }

    ret |= VI530x_Write_Firmware_Post_Config();
    //此延时是必须的，具体可以改
    delay_ms(100);
    ret |= Get_VI530x_Download_Firmware_Status();
    ret |= VI530x_Bvd_Add(3);
    return ret;
}

